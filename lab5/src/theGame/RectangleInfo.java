package theGame;

public class RectangleInfo {
    double x, y;
    double width, height;
    int type;
    RectangleInfo(double x, double y, double width, double height, int type){
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.type = type;
    }
}
